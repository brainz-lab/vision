  def icon(name)
    ICONS[name.to_sym]&.html_safe
  end

  # Navigation helpers for sidebar
  def nav_active?(path_pattern)
    if path_pattern == "/settings"
      request.path.match?(%r{/projects/[^/]+/settings$})
    else
      request.path.include?(path_pattern)
    end
  end

  def nav_link_class(path_pattern)
    if nav_active?(path_pattern)
      "nav-item active"
    else
      "nav-item"
    end
  end

  # Status badge helpers for test runs and comparisons
  def status_badge_class(status)
    case status.to_s
    when "passed", "approved"
      "badge-green"
    when "failed", "rejected"
      "badge-red"
    when "pending", "running"
      "badge-yellow"
    when "new", "changed"
      "badge-blue"
    else
      "badge-gray"
    end
  end

  def diff_status_class(diff_percentage, threshold = 0.01)
    if diff_percentage.nil?
      "text-stone-500"
    elsif diff_percentage <= threshold
      "text-green-600"
    else
      "text-red-600"
    end
  end
